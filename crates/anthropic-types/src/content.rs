    #[test]
    fn response_content_blocks_convert_to_request_blocks() -> Result<(), Box<dyn std::error::Error>>
    {
        let blocks = [
            ContentBlock::text("Hello"),
            ContentBlock::Thinking {
                thinking: "I should preserve this.".to_owned(),
                signature: Some("ThinkingSignature".to_owned()),
            },
            ContentBlock::redacted_thinking("RedactedPayload"),
            ContentBlock::ToolUse {
                id: "toolu_01".to_owned(),
                name: "get_weather".to_owned(),
                input: serde_json::json!({ "city": "London" }),
            },
        ];

        let params = blocks
            .iter()
            .map(ContentBlockParam::try_from)
            .collect::<Result<Vec<_>, _>>()?;

        assert_eq!(
            params,
            vec![
                ContentBlockParam::text("Hello"),
                ContentBlockParam::thinking_with_signature(
                    "I should preserve this.",
                    "ThinkingSignature"
                ),
                ContentBlockParam::redacted_thinking("RedactedPayload"),
                ContentBlockParam::tool_use(
                    "toolu_01",
                    "get_weather",
                    serde_json::json!({ "city": "London" })
                )?,
            ]
        );
        Ok(())
    }

