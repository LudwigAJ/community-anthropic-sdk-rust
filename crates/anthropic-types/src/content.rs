//! Content blocks for Messages API requests, responses, and streaming.
//!
//! Two parallel block taxonomies live here because the Messages API uses
//! slightly different shapes on the request and response sides:
//!
//! - [`ContentBlockParam`] is the **request-side** tagged union: text,
//!   image (base64 / URL), document (base64 / URL / text / nested
//!   content), search-result, tool-use replay, tool-result, thinking, and
//!   redacted-thinking. Every variant that the API supports carries an
//!   optional [`crate::CacheControl`].
//! - [`ContentBlock`] is the **response-side** tagged union returned in
//!   [`crate::Message::content`], including typed citation arrays on text
//!   blocks.
//!
//! Sources for image and document blocks are typed
//! ([`ImageSourceParam`], [`DocumentSourceParam`], [`DocumentMediaType`],
//! [`ContentBlockSourceContentParam`],
//! [`ContentBlockSourceContentBlockParam`]) rather than raw JSON.
//! [`SearchResultTextBlockParam`] models text-only search-result content.
//!
//! Citations on both sides ([`TextCitation`] /
//! [`TextCitationParam`]) cover `char_location`, `page_location`,
//! `content_block_location`, `web_search_result_location`, and
//! `search_result_location`.
//!
//! [`ToolUse`] is a borrowed view returned by
//! [`crate::Message::tool_uses`], paired with
//! [`ToolInputDecodeError`] for typed input decoding that does **not**
//! leak raw tool-input JSON in `Display`.
//!
//! [`ContentBlockParamConversionError`] and
//! [`ContentBlockParamCacheControlError`] surface the validation failures
//! that builders apply at the request boundary.

use serde::{Deserialize, Serialize, de::DeserializeOwned};
use thiserror::Error;

use crate::{CacheControl, ToolName, ToolNameError};

/// A content block returned by the API.
#[derive(Debug, Clone, PartialEq, Serialize, Deserialize)]
#[serde(tag = "type", rename_all = "snake_case")]
pub enum ContentBlock {
    /// Plain text content.
    Text {
        /// The UTF-8 text payload.
        text: String,
        /// Citations supporting this text block, when returned by the API.
        #[serde(default, skip_serializing_if = "Option::is_none")]
        citations: Option<Vec<TextCitation>>,
    },
    /// Extended thinking content returned by the model.
    Thinking {
        /// The model's thinking text.
        thinking: String,
        /// Signature used by the API for multi-turn thinking continuity, when present.
        #[serde(skip_serializing_if = "Option::is_none")]
        signature: Option<String>,
    },
    /// Redacted extended thinking content returned by the model.
    RedactedThinking {
        /// Opaque redacted thinking payload.
        data: String,
    },
    /// A model tool-use request.
    ToolUse {
        /// API-generated tool-use identifier.
        id: String,
        /// Tool name requested by the model.
        name: String,
        /// JSON input for the selected tool.
        input: serde_json::Value,
    },
    /// A tool result supplied back to the model.
    ToolResult {
        /// Identifier of the tool-use block this result answers.
        tool_use_id: String,
        /// Result content blocks.
        content: Vec<ContentBlock>,
    },
}

impl ContentBlock {
    /// Creates a text content block.
    pub fn text(text: impl Into<String>) -> Self {
        Self::Text {
            text: text.into(),
            citations: None,
        }
    }

    /// Creates a thinking content block without a signature.
    pub fn thinking(thinking: impl Into<String>) -> Self {
        Self::Thinking {
            thinking: thinking.into(),
            signature: None,
        }
    }

    /// Creates a redacted thinking content block.
    pub fn redacted_thinking(data: impl Into<String>) -> Self {
        Self::RedactedThinking { data: data.into() }
    }

    /// Returns this block as a borrowed tool-use view when it is a tool-use block.
    pub fn as_tool_use(&self) -> Option<ToolUse<'_>> {
        match self {
            Self::ToolUse { id, name, input } => Some(ToolUse { id, name, input }),
            _ => None,
        }
    }
}

/// Borrowed view over a response `tool_use` content block.
#[derive(Debug, Clone, Copy, PartialEq)]
pub struct ToolUse<'a> {
    id: &'a str,
    name: &'a str,
    input: &'a serde_json::Value,
}

impl<'a> ToolUse<'a> {
    /// Returns the API-generated tool-use identifier.
    pub fn id(&self) -> &'a str {
        self.id
    }

    /// Returns the requested tool name.
    pub fn name(&self) -> &'a str {
        self.name
    }

    /// Returns the raw JSON input generated by the model.
    pub fn input(&self) -> &'a serde_json::Value {
        self.input
    }

    /// Decodes the tool input into a caller-owned type.
    ///
    /// The returned error keeps the serde source error but its display text does
    /// not include the raw input JSON.
    pub fn decode_input<T>(&self) -> Result<T, ToolInputDecodeError>
    where
        T: DeserializeOwned,
    {
        serde_json::from_value(self.input.clone()).map_err(|source| {
            ToolInputDecodeError::Deserialize {
                tool_use_id: self.id.to_owned(),
                name: self.name.to_owned(),
                source,
            }
        })
    }

    /// Creates a text tool-result block answering this tool use.
    pub fn tool_result(&self, content: impl Into<String>) -> ContentBlockParam {
        ContentBlockParam::tool_result(self.id, content)
    }

    /// Creates a text error tool-result block answering this tool use.
    pub fn tool_result_error(&self, content: impl Into<String>) -> ContentBlockParam {
        ContentBlockParam::tool_result_error(self.id, content)
    }

    /// Creates a structured tool-result block answering this tool use.
    pub fn tool_result_blocks(&self, content: Vec<ContentBlockParam>) -> ContentBlockParam {
        ContentBlockParam::tool_result_blocks(self.id, content)
    }

    /// Creates a structured tool-result block with explicit error status.
    pub fn tool_result_blocks_with_error(
        &self,
        content: Vec<ContentBlockParam>,
        is_error: bool,
    ) -> ContentBlockParam {
        ContentBlockParam::tool_result_blocks_with_error(self.id, content, is_error)
    }
}

/// Errors produced while decoding a response tool-use input.
#[derive(Debug, Error)]
pub enum ToolInputDecodeError {
    /// The tool input could not be deserialized into the requested type.
    #[error("failed to decode input for tool `{name}`")]
    Deserialize {
        /// API-generated tool-use identifier.
        tool_use_id: String,
        /// Tool name requested by the model.
        name: String,
        /// Serde decoding error.
        #[source]
        source: serde_json::Error,
    },
}

/// A content block accepted in message creation parameters.
#[derive(Debug, Clone, PartialEq, Serialize, Deserialize)]
#[serde(tag = "type", rename_all = "snake_case")]
pub enum ContentBlockParam {
    /// Plain text content.
    Text {
        /// The UTF-8 text payload.
        text: String,
        /// Optional citations attached to this text block.
        #[serde(default, skip_serializing_if = "Option::is_none")]
        citations: Option<Vec<TextCitationParam>>,
        /// Optional cache control marker for this text block.
        #[serde(skip_serializing_if = "Option::is_none")]
        cache_control: Option<CacheControl>,
    },
    /// Image content supplied by the caller.
    Image {
        /// The image source.
        source: ImageSourceParam,
        /// Optional cache control marker for this image block.
        #[serde(skip_serializing_if = "Option::is_none")]
        cache_control: Option<CacheControl>,
    },
    /// Document content supplied by the caller.
    Document {
        /// The document source.
        source: DocumentSourceParam,
        /// Optional title for the document.
        #[serde(skip_serializing_if = "Option::is_none")]
        title: Option<String>,
        /// Optional context that helps the model interpret the document.
        #[serde(skip_serializing_if = "Option::is_none")]
        context: Option<String>,
        /// Optional citation configuration for the document.
        #[serde(skip_serializing_if = "Option::is_none")]
        citations: Option<CitationsConfigParam>,
        /// Optional cache control marker for this document block.
        #[serde(skip_serializing_if = "Option::is_none")]
        cache_control: Option<CacheControl>,
    },
    /// Search result content supplied by the caller.
    SearchResult {
        /// Text blocks that make up the search result body.
        content: Vec<SearchResultTextBlockParam>,
        /// Source URL or identifier for the result.
        source: String,
        /// Search result title.
        title: String,
        /// Optional citation configuration for the search result.
        #[serde(skip_serializing_if = "Option::is_none")]
        citations: Option<CitationsConfigParam>,
        /// Optional cache control marker for this search result block.
        #[serde(skip_serializing_if = "Option::is_none")]
        cache_control: Option<CacheControl>,
    },
    /// Extended thinking content supplied back for multi-turn continuity.
    Thinking {
        /// The model's thinking text.
        thinking: String,
        /// Signature used by the API for multi-turn thinking continuity, when present.
        #[serde(skip_serializing_if = "Option::is_none")]
        signature: Option<String>,
    },
    /// Redacted extended thinking content supplied back for multi-turn continuity.
    RedactedThinking {
        /// Opaque redacted thinking payload.
        data: String,
    },
    /// A model tool-use request supplied back for conversation replay.
    ToolUse {
        /// API-generated tool-use identifier.
        id: String,
        /// Tool name requested by the model.
        name: ToolName,
        /// JSON input for the selected tool.
        input: serde_json::Value,
        /// Optional cache control marker for this tool-use replay block.
        #[serde(skip_serializing_if = "Option::is_none")]
        cache_control: Option<CacheControl>,
    },
    /// A tool result supplied by the caller.
    ToolResult {
        /// Identifier of the tool-use block this result answers.
        tool_use_id: String,
        /// Whether the tool call failed.
        #[serde(skip_serializing_if = "Option::is_none")]
        is_error: Option<bool>,
        /// Result content blocks.
        content: ToolResultContent,
        /// Optional cache control marker for this tool-result block.
        #[serde(skip_serializing_if = "Option::is_none")]
        cache_control: Option<CacheControl>,
    },
}

impl ContentBlockParam {
    /// Creates a text content parameter.
    pub fn text(text: impl Into<String>) -> Self {
        Self::Text {
            text: text.into(),
            citations: None,
            cache_control: None,
        }
    }

    /// Creates a text content parameter with citations.
    pub fn text_with_citations(text: impl Into<String>, citations: Vec<TextCitationParam>) -> Self {
        Self::Text {
            text: text.into(),
            citations: Some(citations),
            cache_control: None,
        }
    }

    /// Creates a text content parameter with a cache-control marker.
    pub fn text_with_cache_control(text: impl Into<String>, cache_control: CacheControl) -> Self {
        Self::Text {
            text: text.into(),
            citations: None,
            cache_control: Some(cache_control),
        }
    }

    /// Creates a base64 image content parameter.
    pub fn image_base64(media_type: impl Into<String>, data: impl Into<String>) -> Self {
        Self::Image {
            source: ImageSourceParam::base64(media_type, data),
            cache_control: None,
        }
    }

    /// Creates a URL image content parameter.
    pub fn image_url(url: impl Into<String>) -> Self {
        Self::Image {
            source: ImageSourceParam::url(url),
            cache_control: None,
        }
    }

    /// Creates a base64 PDF document content parameter.
    pub fn document_base64_pdf(data: impl Into<String>) -> Self {
        Self::document(DocumentSourceParam::base64_pdf(data))
    }

    /// Creates a plain text document content parameter.
    pub fn document_text(data: impl Into<String>) -> Self {
        Self::document(DocumentSourceParam::plain_text(data))
    }

    /// Creates a URL PDF document content parameter.
    pub fn document_url(url: impl Into<String>) -> Self {
        Self::document(DocumentSourceParam::url_pdf(url))
    }

    /// Creates a document content parameter from typed document source content.
    pub fn document_content(content: ContentBlockSourceContentParam) -> Self {
        Self::document(DocumentSourceParam::content(content))
    }

    /// Creates a document content parameter from an explicit source.
    pub fn document(source: DocumentSourceParam) -> Self {
        Self::Document {
            source,
            title: None,
            context: None,
            citations: None,
            cache_control: None,
        }
    }

    /// Creates a search result content parameter.
    pub fn search_result(
        content: Vec<SearchResultTextBlockParam>,
        source: impl Into<String>,
        title: impl Into<String>,
    ) -> Self {
        Self::SearchResult {
            content,
            source: source.into(),
            title: title.into(),
            citations: None,
            cache_control: None,
        }
    }

    /// Creates a thinking content parameter without a signature.
    pub fn thinking(thinking: impl Into<String>) -> Self {
        Self::Thinking {
            thinking: thinking.into(),
            signature: None,
        }
    }

    /// Creates a thinking content parameter with a signature.
    pub fn thinking_with_signature(
        thinking: impl Into<String>,
        signature: impl Into<String>,
    ) -> Self {
        Self::Thinking {
            thinking: thinking.into(),
            signature: Some(signature.into()),
        }
    }

    /// Creates a redacted thinking content parameter.
    pub fn redacted_thinking(data: impl Into<String>) -> Self {
        Self::RedactedThinking { data: data.into() }
    }

    /// Creates a tool-use content parameter for assistant message replay.
    pub fn tool_use(
        id: impl Into<String>,
        name: impl TryInto<ToolName, Error = ToolNameError>,
        input: serde_json::Value,
    ) -> Result<Self, ToolNameError> {
        Ok(Self::ToolUse {
            id: id.into(),
            name: name.try_into()?,
            input,
            cache_control: None,
        })
    }

    /// Creates a text tool result content block.
    pub fn tool_result(tool_use_id: impl Into<String>, content: impl Into<String>) -> Self {
        Self::ToolResult {
            tool_use_id: tool_use_id.into(),
            is_error: None,
            content: ToolResultContent::text(content),
            cache_control: None,
        }
    }

    /// Creates a text tool result content block that represents a tool failure.
    pub fn tool_result_error(tool_use_id: impl Into<String>, content: impl Into<String>) -> Self {
        Self::ToolResult {
            tool_use_id: tool_use_id.into(),
            is_error: Some(true),
            content: ToolResultContent::text(content),
            cache_control: None,
        }
    }

    /// Creates a structured tool result content block.
    pub fn tool_result_blocks(
        tool_use_id: impl Into<String>,
        content: Vec<ContentBlockParam>,
    ) -> Self {
        Self::ToolResult {
            tool_use_id: tool_use_id.into(),
            is_error: None,
            content: ToolResultContent::blocks(content),
            cache_control: None,
        }
    }

    /// Creates a structured tool result content block with explicit error status.
    pub fn tool_result_blocks_with_error(
        tool_use_id: impl Into<String>,
        content: Vec<ContentBlockParam>,
        is_error: bool,
    ) -> Self {
        Self::ToolResult {
            tool_use_id: tool_use_id.into(),
            is_error: Some(is_error),
            content: ToolResultContent::blocks(content),
            cache_control: None,
        }
    }

    /// Sets cache control on a cacheable request content block.
    pub fn with_cache_control(
        self,
        cache_control: CacheControl,
    ) -> Result<Self, ContentBlockParamCacheControlError> {
        match self {
            Self::Text {
                text, citations, ..
            } => Ok(Self::Text {
                text,
                citations,
                cache_control: Some(cache_control),
            }),
            Self::Image { source, .. } => Ok(Self::Image {
                source,
                cache_control: Some(cache_control),
            }),
            Self::Document {
                source,
                title,
                context,
                citations,
                ..
            } => Ok(Self::Document {
                source,
                title,
                context,
                citations,
                cache_control: Some(cache_control),
            }),
            Self::SearchResult {
                content,
                source,
                title,
                citations,
                ..
            } => Ok(Self::SearchResult {
                content,
                source,
                title,
                citations,
                cache_control: Some(cache_control),
            }),
            Self::ToolUse {
                id, name, input, ..
            } => Ok(Self::ToolUse {
                id,
                name,
                input,
                cache_control: Some(cache_control),
            }),
            Self::ToolResult {
                tool_use_id,
                is_error,
                content,
                ..
            } => Ok(Self::ToolResult {
                tool_use_id,
                is_error,
                content,
                cache_control: Some(cache_control),
            }),
            Self::Thinking { .. } => Err(
                ContentBlockParamCacheControlError::UnsupportedContentBlock {
                    block_type: "thinking",
                },
            ),
            Self::RedactedThinking { .. } => Err(
                ContentBlockParamCacheControlError::UnsupportedContentBlock {
                    block_type: "redacted_thinking",
                },
            ),
        }
    }
}

/// A citation attached to a response text block.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
#[serde(tag = "type", rename_all = "snake_case")]
pub enum TextCitation {
    /// Citation into a plain text document by character offsets.
    #[serde(rename = "char_location")]
    Char {
        /// Text cited by the model.
        cited_text: String,
        /// Zero-based index of the cited document block in the request.
        document_index: u32,
        /// Title of the cited document, when available.
        document_title: Option<String>,
        /// End character offset.
        end_char_index: u32,
        /// File identifier for hosted/container-backed documents, when returned.
        file_id: Option<String>,
        /// Start character offset.
        start_char_index: u32,
    },
    /// Citation into a PDF document by page numbers.
    #[serde(rename = "page_location")]
    Page {
        /// Text cited by the model.
        cited_text: String,
        /// Zero-based index of the cited document block in the request.
        document_index: u32,
        /// Title of the cited document, when available.
        document_title: Option<String>,
        /// Ending page number.
        end_page_number: u32,
        /// File identifier for hosted/container-backed documents, when returned.
        file_id: Option<String>,
        /// Starting page number.
        start_page_number: u32,
    },
    /// Citation into a content-document source by content-block offsets.
    #[serde(rename = "content_block_location")]
    ContentBlock {
        /// Text cited by the model.
        cited_text: String,
        /// Zero-based index of the cited document block in the request.
        document_index: u32,
        /// Title of the cited document, when available.
        document_title: Option<String>,
        /// End content-block index.
        end_block_index: u32,
        /// File identifier for hosted/container-backed documents, when returned.
        file_id: Option<String>,
        /// Start content-block index.
        start_block_index: u32,
    },
    /// Citation into a web-search result.
    #[serde(rename = "web_search_result_location")]
    WebSearchResult {
        /// Text cited by the model.
        cited_text: String,
        /// Opaque encrypted search-result index.
        encrypted_index: String,
        /// Title of the cited result, when available.
        title: Option<String>,
        /// URL of the cited result.
        url: String,
    },
    /// Citation into a request `search_result` block.
    #[serde(rename = "search_result_location")]
    SearchResult {
        /// Text cited by the model.
        cited_text: String,
        /// End content-block index inside the search result.
        end_block_index: u32,
        /// Zero-based index of the cited search-result block in the request.
        search_result_index: u32,
        /// Source URL or identifier for the cited search result.
        source: String,
        /// Start content-block index inside the search result.
        start_block_index: u32,
        /// Title of the cited result, when available.
        title: Option<String>,
    },
}

impl TextCitation {
    /// Converts this response citation into a request citation parameter.
    pub fn into_param(self) -> TextCitationParam {
        match self {
            Self::Char {
                cited_text,
                document_index,
                document_title,
                end_char_index,
                file_id: _,
                start_char_index,
            } => TextCitationParam::Char {
                cited_text,
                document_index,
                document_title,
                end_char_index,
                start_char_index,
            },
            Self::Page {
                cited_text,
                document_index,
                document_title,
                end_page_number,
                file_id: _,
                start_page_number,
            } => TextCitationParam::Page {
                cited_text,
                document_index,
                document_title,
                end_page_number,
                start_page_number,
            },
            Self::ContentBlock {
                cited_text,
                document_index,
                document_title,
                end_block_index,
                file_id: _,
                start_block_index,
            } => TextCitationParam::ContentBlock {
                cited_text,
                document_index,
                document_title,
                end_block_index,
                start_block_index,
            },
            Self::WebSearchResult {
                cited_text,
                encrypted_index,
                title,
                url,
            } => TextCitationParam::WebSearchResult {
                cited_text,
                encrypted_index,
                title,
                url,
            },
            Self::SearchResult {
                cited_text,
                end_block_index,
                search_result_index,
                source,
                start_block_index,
                title,
            } => TextCitationParam::SearchResult {
                cited_text,
                end_block_index,
                search_result_index,
                source,
                start_block_index,
                title,
            },
        }
    }
}

impl From<TextCitation> for TextCitationParam {
    fn from(value: TextCitation) -> Self {
        value.into_param()
    }
}

