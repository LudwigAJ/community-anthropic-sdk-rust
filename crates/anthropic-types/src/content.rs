impl TryFrom<ContentBlock> for ContentBlockParam {
    type Error = ContentBlockParamConversionError;

    fn try_from(value: ContentBlock) -> Result<Self, Self::Error> {
        match value {
            ContentBlock::Text { text, citations } => Ok(Self::Text {
                text,
                citations: citations.map(|items| items.into_iter().map(Into::into).collect()),
                cache_control: None,
            }),
            ContentBlock::Thinking {
                thinking,
                signature,
            } => Ok(Self::Thinking {
                thinking,
                signature,
            }),
            ContentBlock::RedactedThinking { data } => Ok(Self::redacted_thinking(data)),
            ContentBlock::ToolUse { id, name, input } => {
                Self::tool_use(id, name, input).map_err(ContentBlockParamConversionError::ToolName)
            }
            ContentBlock::ToolResult { .. } => {
                Err(ContentBlockParamConversionError::UnsupportedContentBlock {
                    block_type: "tool_result",
                })
            }
        }
    }
}

impl TryFrom<&ContentBlock> for ContentBlockParam {
    type Error = ContentBlockParamConversionError;

    fn try_from(value: &ContentBlock) -> Result<Self, Self::Error> {
        match value {
            ContentBlock::Text { text, citations } => Ok(Self::Text {
                text: text.clone(),
                citations: citations
                    .clone()
                    .map(|items| items.into_iter().map(Into::into).collect()),
                cache_control: None,
            }),
            ContentBlock::Thinking {
                thinking,
                signature,
            } => Ok(Self::Thinking {
                thinking: thinking.clone(),
                signature: signature.clone(),
            }),
            ContentBlock::RedactedThinking { data } => Ok(Self::redacted_thinking(data.clone())),
            ContentBlock::ToolUse { id, name, input } => {
                Self::tool_use(id.clone(), name.as_str(), input.clone())
                    .map_err(ContentBlockParamConversionError::ToolName)
            }
            ContentBlock::ToolResult { .. } => {
                Err(ContentBlockParamConversionError::UnsupportedContentBlock {
                    block_type: "tool_result",
                })
            }
        }
    }
}

/// Errors produced while converting response content blocks into request blocks.
#[derive(Debug, Clone, PartialEq, Eq, Error)]
pub enum ContentBlockParamConversionError {
    /// The response content block has no conservative request-side equivalent.
    #[error("content block type `{block_type}` cannot be converted to a request content block")]
    UnsupportedContentBlock {
        /// Wire `type` value of the unsupported content block.
        block_type: &'static str,
    },
    /// The response tool-use block contained an invalid tool name.
    #[error(transparent)]
    ToolName(#[from] ToolNameError),
}

/// Errors produced while adding cache control to request content blocks.
#[derive(Debug, Clone, PartialEq, Eq, Error)]
pub enum ContentBlockParamCacheControlError {
    /// The stable Messages API does not expose cache control on this block type.
    #[error("content block type `{block_type}` does not support cache_control")]
    UnsupportedContentBlock {
        /// Wire `type` value of the unsupported content block.
        block_type: &'static str,
    },
}

